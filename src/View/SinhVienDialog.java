   public void fillDateTABLE(){
    DefaultTableModel model = (DefaultTableModel) tblSinhVien.getModel();
    model.setRowCount(0);
    for (SinhVien sv : dao.getAllSinhViens()) {
        Object rowData[] = new Object[6];
        rowData[0] = sv.getMasv();
        rowData[1] = sv.getTensv();
        rowData[2] = date_Format.format(sv.getNgaysinh());
        rowData[3] = sv.getLop();
        rowData[4] = sv.getKhoa();
        rowData[5] = sv.isGioitinh() ? "Nam" : "Nữ";
        model.addRow(rowData);
    }
}


