    public void setModel(SinhVien sv){
        txtMasv.setText(sv.getMasv());
        txtTensv.setText(sv.getTensv());
        txtNgaysinh.setText(date_Format.format(sv.getNgaysinh()));
        boolean gt = sv.isGioitinh();
        if(sv.isGioitinh()){
            rbnam.isSelected();
        }else{
            rbNu.isSelected(); 
        }
    }
    private void tblSinhVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSinhVienMouseClicked
        // TODO add your handling code here:
        int rowIndex = tblSinhVien.getSelectedRow();
if (rowIndex >= 0) {
    DefaultTableModel model = (DefaultTableModel) tblSinhVien.getModel();
    String maSV = model.getValueAt(rowIndex, 0).toString();
    SinhVien sv = dao.getSinhVienByID(maSV);
    
    if (sv != null) {
        txtMasv.setText(sv.getMasv()); // Sửa thành txtMasv.setText(sv.getMasv());
        txtTensv.setText(sv.getTensv());
        txtNgaysinh.setText(dateToString(sv.getNgaysinh())); // Sửa thành txtNgaysinh.setText(dateToString(sv.getNgaysinh()));
        if (sv.isGioitinh()) {
            rbnam.setSelected(true);
            rbNu.setSelected(false);
        } else {
            rbNu.setSelected(true);
            rbnam.setSelected(false);
        }
        txtLop.setText(sv.getLop());
        txtKhoa.setText(sv.getKhoa());
    } else {
        JOptionPane.showMessageDialog(this, "Không tìm thấy sinh viên");
      }
    }//GEN-LAST:event_tblSinhVienMouseClicked
  }
    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        if (txtMasv.getText().isEmpty()) {
            JOptionPane.showConfirmDialog(this,"Bạn chưa nhập mã sinh viên để xóa");
        }else{
            if(dao.delSinhVienByID(txtMasv.getText()) > 0){
            JOptionPane.showConfirmDialog(this, "Xóa thành công");
            fillDateTABLE();
            }else{
                JOptionPane.showConfirmDialog(this, "Không tìm thấy sinh viên");
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

