  public SinhVien getModel() throws ParseException {
    SinhVien sv = new SinhVien();
    sv.setMasv(txtMasv.getText());
    sv.setTensv(txtTensv.getText());
    sv.setGioitinh(rbnam.isSelected()); // Sử dụng trực tiếp isSelected() của radio button
    sv.setLop(txtLop.getText());
    sv.setKhoa(txtKhoa.getText());
    sv.setNgaysinh(date_Format.parse(txtNgaysinh.getText()));
    return sv;
}

    
